      <div class="exercise">
        <p>Memorizing gym equipment names and what they look like when starting your journey through fitness and putting together an exercise regiment can seem pretty daunting in the beginning.</p>

        <p>We’ve put together this exercise equipment list to set you free from the unknown and expand your fitness horizons. This guide will help you put together an effective and informed workout regiment.</p>

        <br>
        
        <h3 style="font-family: arial">Gym Equipment Pictures & Explanations</h3>
        <p>We hope this helps you navigate your gym of choice with a little more confidence. Find out what each gym machine is called, what it looks like, and how to use them.</p>

        <br>

        <h4 style="font-family: arial">SQUAT RACK</h4>
        <br>

        <img class="image rounded mx-auto d-block" src="image/squat.jpg">
        <br>

        <p><span style="font-weight: bold;">Description:</span> Where serious squatting takes place. In fitness and strength training, the squat exercise trains your full body.  All serious strength training regiments should incorporate the squat station gym equipment.</p>

         <p><span style="font-weight: bold;">Pro Tip:</span> Be sure to squat down until your thighs are pretty much parallel with the floor. Going this low is essential in doing squats the right way and obtaining a strong core, legs, and back eventually.</p>

         <p>Try to keep your knees from extending past your toes, your butt should be out and back straight when doing squats. Do not arch your back, keep your head up and your abs tight to help you with this.</p>

         <p>Your feet positioning should be just slightly wider than hip-width, and both of your feet should be pointing slightly outward.</p>

         <p><span style="font-weight: bold;">Muscles Used:</span> Squatting focuses primarily on thighs, hips, and butt, quads, hamstrings; all while strengthening your bones and ligaments.</p>

         <br>

          <img class="image rounded mx-auto d-block" src="image/squat2.jpg">

          <br>

          <h4 style="font-family: arial">BARBELLS</h4>
          <br>

          <img class="image rounded mx-auto d-block" src="image/barbell.jpg">
          <br>

          <p><span style="font-weight: bold;">Description:</span> A complementary piece to the squat rack. A barbell is essential to strength training, it holds the free weights, or sometimes the weights are attached to the ends.</p>

           <p><span style="font-weight: bold;">Pro Tip:</span> A barbell is perhaps the most versatile out of all the gym equipment out there. You can literally do hundreds of different exercises with these iron bars.</p>

           <p>A common technique to use when utilizing a barbell is good posture and keeping your body from swaying; in order to make sure that your body stays stabilized, as when doing exercises on a gym machine for example.</p>

           <p>Because it is easy to cheat and swing your body whichever way to make the exercise easier, using a barbell comes with a word of caution: injuries.</p>

           <p>It is critical to keep good form when doing barbell exercises, particularly when picking them up off the ground and placing them back down, as during that period the risk of injury is particularly high!</p>

           <p><span style="font-weight: bold;">Muscles Used:</span> Pretty much all muscles are affected when training with barbells, especially if doing deadlifts.</p>

           <br>

            <img class="image rounded mx-auto d-block" src="image/barbell2.png">

            <br>

            <h4 style="font-family: arial">BENCH PRESS</h4>
            <br>

            <img class="image rounded mx-auto d-block" src="image/benchpress.jpg">
            <br>

            <p><span style="font-weight: bold;">Description:</span> Used for upper body strength training exercises, where you are pushing weight upwards as you’re laying on your back. Do you want the perfect chest? This is one of the tools you use to train for that chest.</p>

             <p><span style="font-weight: bold;">Pro Tip:</span> When doing a bench press, do not arch your back, this means that your feet are placed too low. Try not to bounce the bar off your chest either, even though you may think it makes you look super macho.</p>

             <p>Also, do not lower the bar towards your neck or abdomen as it could seriously injure you is you have excessive weight and lose control, even momentarily.</p>

             <p><span style="font-weight: bold;">Muscles Used:</span> Biceps, Triceps, Lats, Shoulders, Chest, and Upper Back.</p>

             <br>

              <img class="image rounded mx-auto d-block" src="image/benchpress1.png">

              <br>

              <h4 style="font-family: arial">INCLINE BENCH PRESS</h4>
              <br>

              <img class="image rounded mx-auto d-block" src="image/inclinebenchpress.jpg">
              <br>

              <p><span style="font-weight: bold;">Description:</span> Essentially an elevated bench press, but the elevation affects the muscle being targeted.</p>

               <p><span style="font-weight: bold;">Pro Tip:</span> Common mistakes people tend to make when exercising with an incline bench press are: setting the bench at a high incline (over 50 degrees), arching their backs, bouncing the bar off their chest, and locking of elbows. Try to avoid making the above errors by keeping good form and keeping the incline of the bench at below 50 degrees.</p>

               <p>Finally, the position of your thumb is important. Grip the bar with your thumbs in opposition to your fingers, this is a more natural grip and safer for lifting.</p>

               <p><span style="font-weight: bold;">Muscles Used:</span> The upper chest is targeted by the incline, as well as shoulders and triceps. It is not uncommon to use dumbbells in junction with or instead of a barbell on an incline bench.</p>

               <br>

                <img class="image rounded mx-auto d-block" src="image/inclinebenchpress1.png">

                <br>

                <h4 style="font-family: arial">HAMMER STRENGTH MACHINE</h4>
                <br>

                <img class="image rounded mx-auto d-block" src="image/hammer.jpg">
                <br>

                <p><span style="font-weight: bold;">Description:</span> A favorite among athletes, this mechanic focuses on explosiveness.</p>

                 <p><span style="font-weight: bold;">Pro Tip:</span> Do not put too much demand on your triceps when exercising with the hammer strength machine. Also avoid locking your elbows at the top of the lift, and try your best to maintain an equal force in both arms as you’re lifting.</p>

                 <p><span style="font-weight: bold;">Muscles Used:</span> Different types target different muscle groups. Primarily chest, then shoulders and triceps; to traps, and others.</p>

                 <br>

                  <img class="image rounded mx-auto d-block" src="image/hammer1.png">

                  <br>

                  <h4 style="font-family: arial">CABLES AND PULLEYS</h4>
                  <br>

                  <img class="image rounded mx-auto d-block" src="image/cables.jpg">
                  <br>

                  <p><span style="font-weight: bold;">Description:</span> Very diverse workout machine in the amount and types of exercises that can be performed by attaching grips to the end of the cables.</p>

                   <p><span style="font-weight: bold;">Pro Tip:</span> When doing cable and pulley machine exercises, it is important to use the right amount of weight. Too little weight and you will not be working out effectively, too much and you will put a strain on your muscles, perhaps even lead to injury.</p>

                   <p><span style="font-weight: bold;">Muscles Used:</span> The inherent design and versatility of this machine allow for it to essentially touch upon every muscle in the body. The example below demonstrates a cable crossover exercise targeting the chest and shoulder areas.</p>

                   <br>

                    <img class="image rounded mx-auto d-block" src="image/cables1.png">

                    <br>

                  <h4 style="font-family: arial">DUMBBELLS</h4>
                  <br>

                  <img class="image rounded mx-auto d-block" src="image/dumbells.jpg">
                  <br>

                  <p><span style="font-weight: bold;">Description:</span> Pretty much the go-to gym equipment most people first think of when they think of bodybuilding. Varying in weight, but the same concept, a handlebar with weights on opposite ends. A must-have free weight for any fitness regiment. There are even adjustable dumbbells with differing weights all in one piece.</p>

                   <p><span style="font-weight: bold;">Pro Tip:</span> Dumbbells have a lot going for them, mainly, they are inexpensive when compared to other equipment. But if used right, they can offer just as much, if not more, than some of their more lucrative gym mates. When working out with dumbbells, try not to lock your elbows at the end of the movement, and do not force the last few repetitions if you do not have a friend or gym buddy to spot you.</p>
                   <p>Dumbbells cause you to work other muscles as you are targeting a primary few; due to your body keeping balance and all the stabilizing muscles coming into play.</p>

                   <p><span style="font-weight: bold;">Muscles Used:</span> You can target a good amount of muscles with dumbbells. Below is an example of a seated dumbbell shoulder press and a bicep curl. These exercises work the biceps, shoulders, and triceps. Some of the muscles you can work with dumbbells are: chest, shoulders, triceps, traps, biceps, lats, glutes, quads, hammies, and calves.</p>

                   <br>

                    <img class="image rounded mx-auto d-block" src="image/dumbells1.jpg">

                    <br>

                     <img class="image rounded mx-auto d-block" src="image/dumbells2.jpg">

                     <br>


                    <h4 style="font-family: arial">PULLUP BAR</h4>
                    <br>

                    <img class="image rounded mx-auto d-block" src="image/pullup.jpg">
                    <br>

                    <p><span style="font-weight: bold;">Description:</span> The best tool for upper body exercise and strengthening. You can pull yourself up with any grip, though palms facing froward is the most popular one seen in demonstrations. Different grips and hand positions can affect different muscles. You pull yourself up until your chin is over the bar.</p>

                     <p><span style="font-weight: bold;">Pro Tip:</span> Pullups are VERY demanding, especially for beginners. It is because of this that you must learn to master them. They are truly an excellent, and yes very demanding, upper body workout. Keep your movements vertical, aligned, and complete when doing pullups. After a while, if you become an expert at it, you can hang weight from your waist for additional resistance.</p>

                     <p>Mistakes include: doing the exercise rapidly, incomplete movement, swinging your body, bending of the torso forward to attain help from your chest muscles, bending knees to get momentum.</p>

                     <p><span style="font-weight: bold;">Muscles Used:</span> Trunk, arms, shoulders, abs, pelvic floor muscles, hands, and forearms.  Different types of pull-ups affect different muscles. Some of the more common types of pullup exercises are: behind the neck, underhand grip, pullup to the waist, wide/butterfly grip.</p>

                     <br>

                      <img class="image rounded mx-auto d-block" src="image/pullup1.jpg">

                      <br>

                      <h4 style="font-family: arial">LEG EXTENSION MACHINE</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/legextension.png">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> Lifting the weights up with your quads, holding steady for a second or two once you fully extend at the top. Leg extension machines are the perfect gym equipment to use when you are rehabilitating from an injury or just trying to strength train your quads.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> Avoid twisting hips and knees as you move the weight, especially when lowering the weight to the machine’s limit. Avoid fast movements just to gain momentum, remember that form is superior to everything else in bodybuilding. Flexing and/or extending the foot does not change the work done by the quadriceps.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Quadriceps, gluteal deltoid,</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/legextension1.png">

                        <br>

                      <h4 style="font-family: arial">LEG CURL MACHINE</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/legcurl.png">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> Laying on your stomach and facing the floor, lifting with your legs upwards towards your lower back. Squeezing at the top and holding for a second or two, feel it in your hamstrings. A good tool to use for either toning down or bulking up your legs.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> When addition to the lying leg curl, some of the more popular exercise types include: toes in, toes out, single-leg, standing single leg, and seated as well. Extending your knees too far as you lower the weight, lifting the weight too fast to gain momentum, and also not placing the knees in line with the axis of the machine are all some of the most common mistakes made by people in the gym using this exercise machine.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Biceps femoris, hamstrings, gracilis, sartorius, gastrocnemius, popliteus</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/legcurl1.png">

                        <br>

                      <h4 style="font-family: arial">DIPPING BARS</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/dips.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> Holding onto the handles, one with each hand, lower your body and lift yourself up. It’s important to adhere to good form to avoid any potential shoulder injuries when dabbling with this gym equipment type.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> This is an excellent alternative to the decline press, but it does require a little more strength. So though typically not suggested for beginners, easing into it however is also not discouraged. When doing dips, you will notice that it feels as if your triceps are doing all the hard work and all the pressure is on them; it is essential however to focus on the pectoral muscle above all.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Spinal erectors, longissimus dorsi, quadratus lumborum, iliocostalis, latissimum dorsi, spinalis, multifidi, gluteus maximus, and hamstrings. In a nutshell back, butt, and hamstrings.</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/dips1.png">

                        <br>

                      <h4 style="font-family: arial">SMITH MACHINE</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/smith.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> A squat assisting workout machine. Think of it as the squat rack that helps you when you don’t have anyone to spot you. Very good machine to use for practicing your squatting form. A go-to machine for back strengthening after an injury of the sort.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> There are many exercises to do on the smith machine that may look the same to you as the other machines; but keep in mind, with the smith machine you can start the exercise from above.</p>

                       <p>A major advantage of the smith machine is for working out in the gym by yourself when there is no one to spot you, but you still want to be safe; by simply turning and locking the bar. This is ideal for training squats on your own, and because of the balance, it provides.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> All muscles can be worked using the smith machine gym equipment. One of the exercises typically done on a smith machine is the military press shown below. It focuses on the shoulder, triceps, and traps.</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/smith1.png">

                        <br>

                      <h4 style="font-family: arial">PREACHER BENCH</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/preacher.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> While seated, the preacher curl bench helps you isolate and train your biceps. A perfect place for beginners to start biceps training by utilizing a lighter barbell and lifting up and down; slowly bringing the weight down.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> Sit with your underarms at the top of the edge and your triceps resting on the preacher bench pad. Whether you are doing one-handed dumbbell exercises, such as hammer grips or one arm twists, or whether you are using a barbell, follow the above technique.</p>

                       <p>Do not extend your arm too far, as this can result in injury.  Also do not shift your body downwards toward the weight, just to get leverage and complete the curl. Most importantly, do not use too much weight and combine that with lowering the bar or dumbbell too far down.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Biceps, brachialis, and forearms.</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/preacher1.png">

                        <br>

                    <h4 style="font-family: arial">ABDOMINAL BENCH/Sit-Up Bench</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/abs.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> Designed with your abs in mind. It’s similar to the hyperextension machine, but for your abs. Important to practice and adhere to proper form when performing this exercise. Especially if you decide to hold onto weights as you’re lifting yourself up.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> Doing this exercise also causes your hips to flex, which brings in the muscles that work that area, the psoas, and the front of the quadriceps in particular. Thus, it is not as specific as just the crunch, so be sure to feel the muscles as you are working out, because more than one group should be involved, and you want to make sure your torso not as rigid, in order to focus more on the abdominal muscles. A few types of sit-ups to try are twisting, weighted, incline bench, and vertical bench.</p>


                       <p><span style="font-weight: bold;">Muscles Used:</span> Abs (rectus abdominis). Also external and internal obliques, psoas, quadriceps, transverse abdominal (pyramidal)</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/abs1.png">

                        <br>

                      <h4 style="font-family: arial">LEG PRESS MACHINE</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/legpress.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> Ultimate leg training machine. Laying down with you back against a makeshift wall, push the platform/weights upwards; do not lock your joints. Perfect machine to train legs with.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> This machine is just as good as the squat machine as far as building strength and developing the muscle, however, it is good for protecting the back, due to the machine (with your help) holding it in position. Common mistakes fitness enthusiasts make when performing the leg press exercise are lifting the hips as the weight is lowered.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Quads (quadriceps), gluteus maximus, adductors, and hamstrings.</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/legpress1.png">

                        <br>


                    <h4 style="font-family: arial">CALF MACHINE</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/calf.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> Lifting the weights at the end by pushing upward with your feet by going on your toes. There are various types of these machines for working out, but the one pictured is by far the most common calf machine in the gym.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> Proper execution entails sitting with your knees bent at a right angle, with the balls of your feet on the footrest. Your heels should be free when working out using the calf machine. Raise your heels as high as possible, pushing the pads on your knees as high as you can; once you reach the peak of how high you can push, hold it there for a moment and then slowly lower your heels. Making sure to breathe in a natural manner throughout the process.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Calves mainly. Secondary muscles include: peroneus longus and brevis, gastrocnemius, tibialis posterior, and plantar flexors.</p>

                       <br>

                        <img class="image rounded mx-auto d-block" src="image/calf1.png">

                        <br>

                      <h4 style="font-family: arial">INDOOR CYCLE BIKE</h4>
                      <br>

                      <img class="image rounded mx-auto d-block" src="image/bike.jpg">
                      <br>

                      <p><span style="font-weight: bold;">Description:</span> This one is pretty simple to understand. It’s basically a more convenient alternative to biking. This is the machine you’ll often see used in spin classes, but you can also use it individually.</p>

                       <p><span style="font-weight: bold;">Pro Tip:</span> Using an indoor cycling bike is similar in some ways to a regular bike but very different in other ways. You want to make sure you are staying safe to prevent injuries. Setting up properly and maintaining proper form is the key to doing this.</p>

                       <p>Do not extend your arm too far, as this can result in injury.  Also do not shift your body downwards toward the weight, just to get leverage and complete the curl. Most importantly, do not use too much weight and combine that with lowering the bar or dumbbell too far down.</p>

                       <p><span style="font-weight: bold;">Muscles Used:</span> Calf – Soleus, and gastrocnemius.
                        Thigh – Hamstrings and quadriceps.
                        Gluts/Buttocks – Gluteus maximus, medius, and minimus.
                        Arms – Biceps, and triceps.
                        Shoulders – Deltoids
                        Foot – Plantar flexors, and dorsiflexors
                      </p>

