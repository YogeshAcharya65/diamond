                <div class="exercise">
                <h3>Some Tips</h3>
                <hr>

                <h4>1. These Exercises Should Be Hard To Complete.</h4>
                <p class="p-3" style="font-size: 15px;">These sets should be hard for you. If you can complete all 3 sets of 10, that means you should increase the weight slightly until you are unable to complete every rep. If you can do both 45 second planks, turn them into 1-minute planks. The beauty of this workout is that you can keep doing it forever, and you'll just keep getting stronger and stronger. The "advanced" version of this workout is 90% the same. If you can nail this routine for 4 weeks straight, you've established a fitness skillset that you can use to reach any fitness goal you desire.</p>

                

                <h4>2. How To Complete A Workout When You Don't Want To.</h4>
                <p class="p-3" style="font-size: 15px;">If you really want to quit mid-workout, it might be because you're slogging through your break times between sets. Try using a stopwatch or your phone clock app to keep yourself at 30 second breaks between every set. Not a second longer. This will make your workout feel harder, more intense, but much quicker. And, short breaks can sometimes be better for building muscle. Many report that, while doing this makes the workout physically harder in the moment, they feel like they did a lot more work when they're done with the exercise.</p>

                

                <h4>3. Don't Take Any Days Off.</h4>
                <div class="p-3" style="font-size: 15px;">
                <p>a. 50 push-ups for time and a brisk 20 minute walk.</p>
                <p>b. 3 sets of 15 burpees, and a brisk 20 minute walk.</p>
                <p>c. Just walk for an hour at a brisk pace.</p>
              </div>

                

                <h4>4. Stay Off Your Phone</h4>
                <p class="p-3" style="font-size: 15px;">If you need to use it as a timer, that's fine. But put it in airplane mode and turn off the wifi. The more you're on your phone, the longer you'll be in the gym, and the more you're going to hate working out. Having your phone on during your workout ruins your exercise intensity, and depletes your willpower. If you need your phone that badly, leave it in your car, and use addiction as motivation to finish the workout as fast as possible.</p>


